export function getDayConfig(day: 1 | 2 | 3 | 4 | 5): DayConfig {
  const config = DAYS.find((d) => d.day === day);
  if (!config) throw new Error(`Нет конфигурации для дня ${day}`);
  return config;
}

/** Реплика Анны Сергеевны в конце дня, зависит от числа ошибок за день. */
export function getEndOfDayLine(mistakes: number): string {
  if (mistakes === 0) {
    return 'Смена отработана чисто, стажёр. Ни одной ошибки. Но расслабляться рано.';
  }
  if (mistakes <= 2) {
    return 'Пара писем ушла не туда. В нашем деле это уже риск. Завтра — внимательнее.';
  }
  return 'Сегодня много ошибок, стажёр. Каждое такое письмо — брешь в обороне компании. Ещё один такой день — и разговор будет другим.';
}
